/**
 * 时间格式转换
 * @param str
 * @returns {*}
 */
export const getDateString = str => {
  let date = new Date();
  if (str) {
    date = new Date(str);
  }
  let result = date.Format('yyyy-MM-dd hh:mm:ss');
  if (isNaN(date.getTime())) {
    result = '-';
  }
  return result;
};

Date.prototype.Format = function(fmt) {
  //author: meizz
  var o = {
    'M+': this.getMonth() + 1, //月份
    'd+': this.getDate(), //日
    'h+': this.getHours(), //小时
    'm+': this.getMinutes(), //分
    's+': this.getSeconds(), //秒
    'q+': Math.floor((this.getMonth() + 3) / 3), //季度
    S: this.getMilliseconds(), //毫秒
  };
  if (/(y+)/.test(fmt))
    fmt = fmt.replace(RegExp.$1, (this.getFullYear() + '').substr(4 - RegExp.$1.length));
  for (var k in o)
    if (new RegExp('(' + k + ')').test(fmt))
      fmt = fmt.replace(
        RegExp.$1,
        RegExp.$1.length == 1 ? o[k] : ('00' + o[k]).substr(('' + o[k]).length)
      );
  return fmt;
};

export const getFirstAndLastMonthDay = (time) => {
  let date = new Date();
  if(time){
    date = new Date(time);
  }
  const year = date.getFullYear();
  let month = date.getMonth()+1;
  let da = date.getDate();
  let da1 = date.getDate()-1;
  if(month < 10) month = `0${month}`;
  if(da < 10) da = `0${da}`;
  if(da1 < 10) da1 = `0${da1}`;

  // 当月的第一天
  const   firstdate = `${year}-${month}-01`;
  //获取当月最后一天日期
  let  day = new Date(year,month,0).getDate();
  if(day < 10) day = `0${day}`;
  const lastdate = `${year}-${month}-${day}`;

  const today = `${year}${month}${da}`; // 今天
  const yesterday = `${year}${month}${da1}`; // 昨天

  return {
    firstdate,
    lastdate,
    today,
    yesterday
  };
};
